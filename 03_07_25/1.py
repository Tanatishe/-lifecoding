"""
На вход поступают три целых числа — стороны треугольника (в произвольном порядке).

Ваша задача — проверить, образуют ли эти стороны прямоугольный треугольник.

Программа должна вывести:
    True, если данные стороны образуют прямоугольный треугольник;
    False, если не образуют.

Для проверки пригодится теорема Пифагора:
    гипотенуза^2 = катет1^2 + катет2^2

Сделать задачу необходимо без использования условного оператора (if).
"""


def is_right_triangle(a: int, b: int, c: int) -> bool:
    return a**2 + b**2 + c**2 - max(a, b, c) ** 2 == max(a, b, c) ** 2


# Тесты
triangle_tests = [
    (3, 4, 5, True),  # классический прямоугольный
    (5, 12, 13, True),  # прямоугольный
    (6, 8, 10, True),  # прямоугольный
    (7, 24, 25, True),  # прямоугольный
    (1, 1, 1, False),  # не прямоугольный
    (2, 2, 3, False),  # не прямоугольный
    (10, 6, 8, True),  # прямоугольный, стороны в другом порядке
    (9, 12, 15, True),  # прямоугольный
    (8, 15, 17, True),  # прямоугольный
    (5, 5, 7, False),  # не прямоугольный
]

for idx, (a, b, c, expected) in enumerate(triangle_tests, 1):
    result = is_right_triangle(a, b, c)
    print(f"Test {idx}: {a}, {b}, {c} → {result} (ожидалось: {expected})")
    assert result == expected

print("Все тесты для прямоугольного треугольника пройдены!")
